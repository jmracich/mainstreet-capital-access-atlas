PRIORITY_DESCRIPTION_SUFFIX = (
    " Winsorized percentile priority value where 100 means stronger local verification priority "
    "for this specific indicator, based on the indicator direction in the methodology."
)


def dictionary_records(column_order: list[str]) -> list[dict[str, Any]]:
    missing = [column for column in column_order if not _has_definition(column)]
    if missing:
        joined = ", ".join(missing)
        msg = f"Public data dictionary is missing definitions for: {joined}"
        raise ValueError(msg)

    return [_record_for_column(column) for column in column_order]


def write_data_dictionary(column_order: list[str]) -> list[dict[str, Any]]:
    records = dictionary_records(column_order)
    csv_path = PUBLIC_DATA_DIR / "data_dictionary.csv"
    json_path = PUBLIC_DATA_DIR / "data_dictionary.json"
    csv_path.parent.mkdir(parents=True, exist_ok=True)
    with csv_path.open("w", encoding="utf-8", newline="") as file:
        writer = csv.DictWriter(file, fieldnames=DICTIONARY_COLUMNS)
        writer.writeheader()
        writer.writerows(records)
    write_json(json_path, records)
    return records


def _record_for_column(column: str) -> dict[str, Any]:
    if column.endswith("_priority"):
        base = column.removesuffix("_priority")
        base_record = FIELD_DEFINITIONS[base]
        return {
            "column_name": column,
            "display_name": f"{base_record['display_name']} priority",
            "category": "Normalized priority indicators",
            "source": f"Derived from {base_record['source']}",
            "unit": "0-100 percentile signal",
            "used_in_signal": True,
            "nullable": True,
            "description": f"Normalized priority version of `{base}`.{PRIORITY_DESCRIPTION_SUFFIX}",
        }

    record = FIELD_DEFINITIONS[column]
    return {"column_name": column, **record}


def _has_definition(column: str) -> bool:
    if column in FIELD_DEFINITIONS:
        return True
    if column.endswith("_priority"):
        return column.removesuffix("_priority") in FIELD_DEFINITIONS
    return False
